// Handle the AJAX request to filter products
function filter_products_by_category() {
    
    // Get the selected category from AJAX request
    $category_slug = isset($_POST['category']) ? sanitize_text_field($_POST['category']) : '';

    // Setup the query args
    $args = array(
        'post_type' => 'product',
        'posts_per_page' => -1,  // Show all products
    );

    // If a category is selected, add a taxonomy query
    if (!empty($category_slug)) {
        $args['tax_query'] = array(
            array(
                'taxonomy' => 'product_cat',
                'field'    => 'slug',
                'terms'    => $category_slug,
            ),
        );
    }

    // Perform the query
    $query = new WP_Query($args);

    if ($query->have_posts()) :
        while ($query->have_posts()) : $query->the_post();
            ?>
             <div class="col-sm-4 mb-4">
             <a href="<?php the_permalink(); ?>"> <!-- Ensures proper spacing and alignment -->
                    <div class="ht-product">
                        <div class="ht-product-inner">
                            <div class="ht-product-image-wrap">
                                <div class="ht-product-image">
                                    <a href="<?php the_permalink(); ?>">
                                        <?php woocommerce_template_loop_product_thumbnail(); ?>
                                    </a>
                                </div>
                            </div>
                            <div class="ht-product-content">
                                <div class="ht-product-content-inner">
                                    <h4 class="ht-product-titles">
                                        <a href="<?php the_permalink(); ?>" class="title_product" style="text-decoration:none"><?php the_title(); ?></a>
                                    </h4>
                                    <div class="ht-product-price">
                                        <span class="price"><?php echo wc_price(get_post_meta(get_the_ID(), '_price', true)); ?></span>
                                    </div>
                                    <!-- Short Description -->
                                    <div class="ht-product-description">
                                    <p>
                                        <?php
                                        // Display short description with ellipsis for long content
                                        $short_desc = wp_strip_all_tags(get_the_excerpt());
                                        echo wp_trim_words($short_desc, 20, '[...]');  // Limits to 20 words
                                        ?>
                                    </p>
                                </div>
                                </div>
                            </div>
                        </div>
                    </div>
                    </a></div>
            <?php
        endwhile;
    else :
        echo '<p>No products found.</p>';
    endif;

    wp_reset_postdata();
    wp_die();  // Required to end the AJAX call properly
}
add_action('wp_ajax_filter_products', 'filter_products_by_category');
add_action('wp_ajax_nopriv_filter_products', 'filter_products_by_category');



function custom_category_filter_shortcode() {
    ob_start(); // Start output buffering
    
    // Include the template file
    get_template_part('template-parts/category-filter');
    
    return ob_get_clean(); // Return the buffered content
}
add_shortcode('categorsy_filter', 'custom_category_filter_shortcode');

// Enqueue the AJAX script and localize the AJAX URL
function enqueue_ajax_product_filter() {
    wp_enqueue_script('ajax-filter', get_template_directory_uri() . '/assets/js/ajax-filter.js', array('jquery'), null, true);
    wp_localize_script('ajax-filter', 'ajaxfilter', array(
        'ajaxurl' => admin_url('admin-ajax.php')  // Set AJAX URL for WordPress
    ));
}
add_action('wp_enqueue_scripts', 'enqueue_ajax_product_filter');


